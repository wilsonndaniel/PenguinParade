
public class Obstacles {
	private int x;
	private int width;
	private int y;
	private int height;
	public Obstacles(int x, int width, int y, int height) {
		this.x = x;
		this.width = width;
		this.y = y;
		this.height = height;
	}
	
	public void setX(int i) {
		x = i;
	} 	
	public int getX() {
		return x;
	} 	
	public void setWidth(int i) {
		width = i;
	} 	
	public int getWidth() {
		return width;
	} 
	public void setY(int i) {
		y = i;
	} 	
	public int getY() {
		return y;
	} 	
	public void setHeight(int i) {
		height = i;
	} 	
	public int getHeight() {
		return height;
	} 
	

}
